package headfirstdesignpattern.CommandPattern;

/**
 * Created by ZhiHui Liu on 2020/4/5.
 */
public class LightOffCommand implements Command {
    Light light;

    public LightOffCommand(Light light) {
        this.light = light;
    }

    @Override
    public void execute() {
        light.off();
    }

    @Override
    public void undo() {
        light.on();
    }
}
